								<div class="row" style="line-height: 21px;font-size: 14px;" id="main_course_introduce_p">
									<p>&nbsp;&nbsp;&nbsp;&nbsp;数据结构是软件工程专业的基石。数据结构是让算机存储、组织数据的方式。数据结构是指相互之间存在一种或多种特定关系的数据元素的集合。通常情况下，精心选择的数据结构可以带来更高的送行或者存储效率。数据结构往往同高效的检素算法和索引技术有关。</p>
									<!-- <br /> -->
									<p>&nbsp;&nbsp;&nbsp;&nbsp;<span class="main-course-introduce-p-blod-font">Sartaj Sahni</span>在他的《数据结构﹑算法与应用》―书中称:“数据结构是<span class="main-course-introduce-p-theme-font">数据对象</span>以及存在于该对象的实例和组成
									<span class="main-course-introduce-p-theme-font">实例</span>的数据元素之间的各种联系。这些联系可以通过定义相关的<span class="main-course-introduce-p-theme-font">函数</span>来给出。”他<span class="main-course-introduce-p-blod-font">将数据对象</span>(data object〉定义为“一个数据对象是实例或值的集合”。</p>
									<!-- <br /> -->
									<p>&nbsp;&nbsp;&nbsp;&nbsp;<span class="main-course-introduce-p-blod-font">clifford A. Shaffer</span>在《<span class="main-course-introduce-p-theme-font">数据结构与算法分析</span>》一书中的定义是:“数据结构是<span class="main-course-introduce-p-blod-font">ADT</span>
									《<span class="main-course-introduce-p-theme-font">抽象数据类型</span>Abstract Data Type〉的物理实现。”</p>
									<!-- <br /> -->
									<p>&nbsp;&nbsp;&nbsp;&nbsp;<span class="main-course-introduce-p-blod-font">Lobert L. Kruse</span>在《数据结构与程序设计》一书中，将一个数据结构的设计过程分成抽象层﹑数据结构层和实现层。其中，抽象层是指抽象数据类型层，它讨论数据的<span class="main-course-introduce-p-theme-font">逻辑结构</span>及其<span class="main-course-introduce-p-theme-font">运算</span>，数据结构层和实现层讨论一个数据结构的表示和在计算机内的存储细节以及运算的实现。</p>
									<!-- <br /> -->
									<p>&nbsp;&nbsp;&nbsp;&nbsp;数据结构具体指同一类数据元素中。各元素之间的相互<span class="main-course-introduce-p-theme-font">关系</span>，包括三个组成<span class="main-course-introduce-p-theme-font">成分</span>，<span class="main-course-introduce-p-theme-font">数据的逻辑结构</span>，<span class="main-course-introduce-p-theme-font">数据</span>的存储结构和数据运算结构。</p>
